    void addToCart(){
         if(rate==0 ||  txtQty.getText().isEmpty() || lblAmount.getText().isEmpty()  ){
            JOptionPane.showMessageDialog(this, "Please select Item and enter Quantity");return;
        };
        String item=lblDes.getText(),
              qty=txtQty.getText(),
              amount=lblAmount.getText();
       DefaultTableModel model = (DefaultTableModel) tableCart.getModel();
       model.addRow(new Object[]{item,qty,rate,amount});
       
       //update quantity
       updateQty(0,item,qty);
       
       lblDes.setText("");
       txtQty.setText("");
       lblAmount.setText("");
       lblRate.setText("");
       
       totalQty=0;
       rate=0.00;
       
       calculateNet();
       getTableData(sqlAllItems,tableItems);
    }
    
    void removeFromCart(){
          int   r= tableCart.getSelectedRowCount();
        DefaultTableModel model = (DefaultTableModel) tableCart.getModel();
        for(int i=0;i<r ;i++){
            int ii=tableCart.getSelectedRow();
           //update quantity to previous 
            String des=model.getValueAt(ii, 0).toString(),
                    qty=model.getValueAt(ii, 1).toString();
            updateQty(1,des,qty);
            model.removeRow(ii);    
        }
        
        getTableData(sqlAllItems,tableItems);
        calculateNet();
    }
    
   void updateQty(int i, String des,String qty){
       String sql="update stocks set quantity =( select quantity - ? where description =? ) where description=? ";        
       if(i==1){
           sql="update stocks set quantity= ( select quantity + ? where description =? )   where description =? ";
       }
       saveData(sql,new String[]{qty,des,des});
    }
   
