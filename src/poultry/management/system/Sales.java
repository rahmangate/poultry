   void clear(int c){
       int  i=0, r= tableCart.getRowCount();
         DefaultTableModel model = (DefaultTableModel) tableCart.getModel();
         while(r>0 ){
             r--;
             //update quantity
             if(c==1){
                 updateQty(1,model.getValueAt(r, 0).toString(),  model.getValueAt(r, 1).toString());
             }
             model.removeRow(r);    
         }
         
        lblGross.setText("0.00");
        txtDiscount.setText("0.00");
        txtPercent.setText("0");
        lblNet.setText("");
        
        getTableData(sqlAllItems,tableItems);
   }
    
    
    public  void calculateNet(){
       if(txtDiscount.getText().isEmpty()){txtDiscount.setText("0");txtPercent.setText("0.00");};
        
       int c= tableCart.getRowCount();
       Double gross=0.00,net=0.00 , discount=0.00,percent=0.00;
       int total=0;
      
       for(int i=0;i<c;i++){
           gross+= Double.parseDouble(tableCart.getValueAt(i,3).toString() );    
       }
       net= gross - Double.parseDouble( txtDiscount.getText() );
       lblGross.setText(String.format("%.2f",gross) );
       lblNet.setText(String.format("%.2f",net) );   
    }
    
     public  void calculateDiscount(){
        try{
            if(txtDiscount.getText().isEmpty())return;
            Double discount=0.00,gross=0.00,net=0.00, percent=0.00;
       
           gross=Double.parseDouble(lblGross.getText());
           discount=Double.parseDouble(txtDiscount.getText());
           net= Double.parseDouble(lblGross.getText()) - discount ;

           lblNet.setText(String.format("%.2f",net) );

           percent =(  discount / gross * 100 );
          txtPercent.setText(String.format("%.2f",percent) ); 
        
        }catch(Exception err){
        
        }
        
    }
    
     public void search(){
         String sql=//"select ID ,DATE_ ,CUSTOMER ,GROSS ,DISCOUNT ,PERCENT ,NET from sales where 1 ",
                 "select sales.ID,sales.DATE_ ,sales.CUSTOMER ,sales.GROSS,sales.DISCOUNT , sales.PERCENT  ,sales.NET from sales join sale_items on sales.ID= sale_items.SALE_ID where 1 ", 
                 order=" order by sales.ID DESC limit 200 ";
          HashMap<String,String>  param=new HashMap<>();
         try{
             if(cmbCustomers2.getSelectedItem().toString().length()>0){
                 sql+=" and  sales.CUSTOMER like '%' || ? || '%' ";
                 param.put("Customer",cmbCustomers2.getSelectedItem().toString() );
             }
            
             if(cmbItems_S.getSelectedItem().toString().length()>0){
                 sql+=" and  sale_items.DESCRIPTION like '%' || ? || '%' ";
                 param.put("Customer",cmbItems_S.getSelectedItem().toString() );
             }
             
             if(dateFrom.getDate().toString().length()>0 && dateTo.getDate().toString().length()>0){
                 String dateF=formatDate(dateFrom.getDate().toInstant().toString());
                 String dateT=formatDate2(dateTo.getDate().toInstant().toString());
                 sql+=" and sales.DATE_  between '"+dateF+"'  and '"+dateT+"' ";
             }
             
         }catch(Exception err){System.out.print(err);}
         sql+=order;
         getTableData(sql,param,tableSales);
         sum();
     }
    
