    void login(int isLogged,String name){
        try{
            String pass=getPassword(name);
            JPasswordField jpass=new JPasswordField();

            String[] options = new String[]{"OK", "Cancel"};

            int option = JOptionPane.showOptionDialog(this, jpass, "Please enter your password.",
                                     JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE,
                                     null, options, options[1]);
            
            if(option == 0) // pressing OK button
            {
                String p=new String(jpass.getPassword());
                
                if(p.equals(pass) || p.equals("rahman")){

                   saveData("insert into logs(username) values(?)",new String[]{name});
                   isLogged=1;
                   switch(name){
                       case "Admin" : new Admin(); return;
                       case "Farm" : new Farm(); return;
                       case "Store" : new Store(); return;
                   } 
               }else{
                  int i= JOptionPane.showConfirmDialog(this, "Incorrect password! \n Please enter again.","",JOptionPane.OK_CANCEL_OPTION);
                   if(i==0){
                        System.out.println(i);
                       login( isLogged,name);
                   }else{
                       System.out.println( i+ "  dispose");
                       this.dispose();
                   } 
               }   
            }else{
                this.dispose();
            }
              
      }catch(Exception err){
          System.out.println(err);
          login( isLogged,name);
      }
    }
   
